// a scipt to find the audio element and fade it in 
// when the page is loaded
document.addEventListener('DOMContentLoaded', function() {
  var audio = document.querySelector('#audio-player-main');
  audio.volume = 0.01;
  audio.play();
  var fadeAudioIn = setInterval(function() {
    if (audio.volume < 1.0) {
        audio.volume += 0.01;
    } else {
        clearInterval(fadeAudioIn);
    }
  }, 50);
});